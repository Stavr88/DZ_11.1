def upper_letter(a: str) -> str:
    """
    Принимает на вход строку и возвращает ее со всеми заглавными буквами
    :param a:str
    :return:str
    """
    return a.upper()


def title_letter(word: str) -> str:
    """
    Делает заглавными первые буквы каждого слова в строке
    :param word:str
    :return:str
    """
    return word.title()

